pub enum TrapirImgType {
    // Represents a JPEG image type.
    Jpeg,
    // Represents a PNG image type.
    Png,
    // Represents a GIF image type.
    Gif,
    // Represents a BMP image type.
    Bmp,
    // Represents a TIFF image type.
    Tiff,
    // Represents an SVG image type.
    Webp,
    // Represents an unknown or unsupported image type.
    Unknown,
}
